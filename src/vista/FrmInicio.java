    public void llamadas(int x){
        switch(x){
            case 1:
               frmAlm.setVisible(true);
               this.dispose();
               break;
            case 2:
                frmMatr.setVisible(true);
                this.dispose();
                break;
            case 3:
                frmCur.setVisible(true);
                this.dispose();
                break;
            case 4:
                frmN.setVisible(true);
                this.dispose();
                break;
        }
    }
    
    

